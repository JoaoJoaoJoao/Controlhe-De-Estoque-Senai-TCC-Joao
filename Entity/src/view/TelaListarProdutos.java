    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir?");
        if (resposta == 0) {

            Produto produtos = new Produto();
            produtos = controler.getByName(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            JOptionPane.showMessageDialog(rootPane, controler.delete(produtos));
            listarProdutos(listaProdutos = controler.getAll());
        }    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int selecionado = jTable1.getSelectedRow();

        String nome = jTable1.getValueAt(selecionado, 0).toString();

        Produto produtos = controler.getByName(nome);

        if (produtos == null) {
            JOptionPane.showMessageDialog(null, "Selecione um item da tabela");
        } else {

            TelaDadosProduto tl = new TelaDadosProduto();
            tl.setVisible(true);
        }

        listaProdutos = controler.getAll();
        listarProdutos(listaProdutos);


    }//GEN-LAST:event_jButton3ActionPerformed

